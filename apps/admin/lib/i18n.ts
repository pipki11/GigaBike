export function isAdminLocale(v: string): v is AdminLocale {
  return (ADMIN_LOCALES as string[]).includes(v);
}

export function makeAt(locale: AdminLocale) {
  const dict = AT[locale] ?? AT[DEFAULT_ADMIN_LOCALE];
  return (key: string, params?: Record<string, string | number>) => {
    let s = dict[key] ?? AT.en[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
      }
    }
    return s;
  };
}

export type AdminT = ReturnType<typeof makeAt>;
